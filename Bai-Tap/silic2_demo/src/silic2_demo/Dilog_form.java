    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int chose = JOptionPane.showConfirmDialog(rootPane, "Are you sure ", "Comfirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if( chose == JOptionPane.YES_OPTION ){
            JOptionPane.showMessageDialog(rootPane, "Bạn chọn nut: Yes");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Bạn chọn nút: NO");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

