    private void Menu_File_OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Menu_File_OpenActionPerformed
        JFileChooser fch = new JFileChooser();
        int k = fch.showOpenDialog(null);
        String sk = "";
        
        if( k == JFileChooser.APPROVE_OPTION){
            try {
                FileReader fw = new FileReader( new File( fch.getSelectedFile().getPath()));
                BufferedReader bff = new BufferedReader(fw);
                String text ;
                while( (text = bff.readLine() ) != null ){
                     sk = sk + text + "\n";
                }
                fw.close();       
            } catch (IOException ex) {
            }
            txtWord.setText(sk);
        }
    }//GEN-LAST:event_Menu_File_OpenActionPerformed

    private void Menu_File_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Menu_File_SaveActionPerformed
        JFileChooser fch = new JFileChooser();
        int k = fch.showSaveDialog(null);
        
        if( k == JFileChooser.APPROVE_OPTION ){
            try {
                FileWriter pw = new FileWriter( new File( fch.getSelectedFile().getPath()) );
                pw.write(txtWord.getText());
                pw.close();
            } catch (FileNotFoundException ex) {
            } catch (IOException ex) {
                Logger.getLogger(Menu_java3.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_Menu_File_SaveActionPerformed

