    public  boolean checkMa(){
        for( ThongTinSP x : listSP ){
            if( x.getMaSP().equalsIgnoreCase(txtMaSP.getText())){
                return false ;
            }
        }
        return true ;
    }
    
    private void btnThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSPActionPerformed
        try {
            if( checkMa() == false ){
                JOptionPane.showMessageDialog(rootPane, "Mã bị trùng rồi");
                return ;
            }
            
            if( txtDonGia.getText().isEmpty() || txtMaSP.getText().isEmpty() || txtNhaCC.getText().isEmpty() || txtTenSP.getText().isEmpty() ){
               JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin");
               return ;
            }
        } catch (Exception e) {
        }
        ThongTinSP sp = new ThongTinSP();
        sp.setMaSP(txtMaSP.getText());
        sp.setTenSP(txtTenSP.getText());
        sp.setDVT(cbbDVT.getItemAt(cbbDVT.getSelectedIndex()));
        sp.setDonGia(Double.valueOf(txtDonGia.getText()));
        sp.setNhaCC(txtNhaCC.getText());
        listSP.add(sp);
        DoVaoTable();
        JOptionPane.showMessageDialog(rootPane, "Thêm Thành Công");
    }//GEN-LAST:event_btnThemSPActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        Index = table.getSelectedRow();
        ThongTinSP sp = listSP.get(Index);
        
        txtMaSP.setText(sp.getMaSP());
        txtDonGia.setText(String.valueOf(sp.getDonGia()));
        txtNhaCC.setText(sp.getNhaCC());
        txtTenSP.setText(sp.getTenSP());
        cbbDVT.setSelectedItem(sp.getDVT());
    }//GEN-LAST:event_tableMouseClicked

    private void btnXoaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaSPActionPerformed
        Index = table.getSelectedRow();
        if( Index < 0 ){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn dòng bạn muốn xóa");
             return ;
        }        
        listSP.remove(Index);
        DoVaoTable();
    }//GEN-LAST:event_btnXoaSPActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Index = table.getSelectedRow();
        if( Index < 0 ){
             JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn dòng bạn muốn sửa");
             return ;
        }
        
        ThongTinSP sp = listSP.get(Index);
        
        sp.setTenSP(txtTenSP.getText());
        sp.setDVT(cbbDVT.getItemAt(cbbDVT.getSelectedIndex()));
        sp.setMaSP(txtMaSP.getText());
        sp.setNhaCC(txtNhaCC.getText());
        sp.setDonGia(Double.valueOf(txtDonGia.getText()));
        DoVaoTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

