    // Cập Nhập Thông Tin 
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String sql = "Update STUDENTS set HoTen = ? , Email = ? , SoDT = ? , GioiTinh = ? , DiaChi = ? where MaSV = ? ";
            try(  Connection con = ConnectSQL.OPenTheLink();
                  PreparedStatement pstmt = con.prepareStatement(sql);
                ) {
                pstmt.setString( 6, txtMaSV.getText());
                pstmt.setString(1, txtHoTen.getText());
                pstmt.setString(2, txtEmail.getText());
                pstmt.setString(3, txtSoDT.getText());
                pstmt.setString(4, CheckNam.isSelected() ? "Nam" : "Nữ");
                pstmt.setString(5, txtDiaChi.getText());
                pstmt.executeUpdate();
                
                JOptionPane.showMessageDialog(rootPane, "Cập Nhập Thành Công");
            }
        } catch (Exception e) {
            e.getStackTrace();
        }
        
        KetNoi();
    }//GEN-LAST:event_btnUpdateActionPerformed

