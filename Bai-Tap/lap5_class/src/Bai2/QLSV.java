    private void KetNoi() {
        try {
            try (
                Connection con = (Connection) ConnectSQL.OPenTheLink();  
                Statement stmt = con.createStatement();
                ) {
                String sql = "select * from STUDENTS";
                ResultSet rs = stmt.executeQuery(sql);
                listSV.clear();
                while (rs.next()) {
                    SinhVien sv = new SinhVien();
                    sv.setMaSV(rs.getString(1));
                    sv.setHoTen(rs.getString(2));
                    sv.setEmail(rs.getString(3));
                    sv.setSoDT(rs.getString(4));
                    sv.setGioiTinh(rs.getString(5));
                    sv.setDiaChi(rs.getString(6));
                    listSV.add(sv);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void ShowInformation( int k){
        SinhVien sv = listSV.get(k);
        txtMaSV.setText(sv.getMaSV());
        txtHoTen.setText(sv.getHoTen());
        txtEmail.setText(sv.getEmail());
        txtSoDT.setText(sv.getSoDT());
        txtDiaChi.setText(sv.getDiaChi());
        if( sv.getGioiTinh().equals("Nam") ) CheckNam.setSelected(true) ;
        else  CheckNu.setSelected(true) ;
    }
