    private int Vitri(){
        for( int i=0 ; i<listSV.size() ; i++  ){
            if( txtMaSV.getText().equalsIgnoreCase( listSV.get(i).getMaSV()) ){
                return i ;
            }
        }
        return -1 ;
    }
    
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if( txtMaSV.getText().isEmpty() ){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập MaSV bạn muốn xóa");
            txtMaSV.requestFocus();
            return ;
        }
        try {
            String sql = "delete from STUDENTS where MaSV = ?";
            try(  Connection con = ConnectSQL.OPenTheLink();
                  PreparedStatement pstmt = con.prepareStatement(sql);
                ) {
                pstmt.setString(1, txtMaSV.getText());
                pstmt.executeUpdate();
                listSV.remove(Vitri());
                JOptionPane.showMessageDialog(rootPane, "Bạn đã xóa thành công");
            }
        } catch (Exception e) {
            e.getStackTrace();
        }
        KetNoi();
    }//GEN-LAST:event_jButton4ActionPerformed

    // Thêm Thông tin SV 
    private boolean CheckMaTrung(){
        for( SinhVien x : listSV ){
            if( txtMaSV.getText().equalsIgnoreCase(x.getMaSV() )){
                return true ;
            }
        }
        return false ;
    }
    
