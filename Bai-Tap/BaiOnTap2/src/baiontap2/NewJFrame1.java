    private void DoVaoCBB () {
        try {
            try (  Connection con = ConnectSQL.OpenLinkSQL();
                   Statement stmt = con.createStatement();
                    ) {
                  String sql = "select MaMH from MONHOC" ;
                  ResultSet rs = stmt.executeQuery(sql);
                  
                  while (rs.next()) {                    
                    model_cbb.addElement(rs.getString("MaMH"));
                }
                
            } 
        } catch (Exception e) {
        }
    }
    
    private void HienThi(){
         try {
             String sql = "select TenMH , diem from MONHOC join DIEM on MONHOC.MaMH = DIEM.MaMH where DIEM.MaMH = ? and Masv = ? ";
             try (   Connection con = ConnectSQL.OpenLinkSQL();
                     PreparedStatement stmt = con.prepareStatement(sql);
                     ){ 
                     stmt.setString(1, vc.maMH );
                     stmt.setString(2, vc.maSS);
                     ResultSet rs = stmt.executeQuery();
                     
                     txtMaSV.setText(vc.maSS);
                     cbbMaMon.setSelectedItem(vc.maMH);
                     while (rs.next() ) {                     
                        txtTenMon.setText(rs.getString("TenMH"));
                        txtDiem.setText( String.valueOf(rs.getString("diem")));                     
                  }

                                      
             } catch (Exception e) {
             }
        } catch (Exception e) {
        }
    }
    
