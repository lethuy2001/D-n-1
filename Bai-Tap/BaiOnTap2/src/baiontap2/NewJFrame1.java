    private boolean CheckMa(){
        try {
            String sql = "select MaMH from DIEM where Masv = ? and MaMH = ? " ;
            try (  Connection con = ConnectSQL.OpenLinkSQL();
                   PreparedStatement stmt = con.prepareCall(sql);
                    ) {
                  stmt.setString(1, txtMaSV.getText());
                  stmt.setString(2, cbbMaMon.getSelectedItem().toString());
                  ResultSet rs = stmt.executeQuery();
                  
                  while ( rs.next() ) {                    
                     if( rs.getString("MaMH").equalsIgnoreCase(cbbMaMon.getSelectedItem().toString())){
                         return true ;
                     }
                }
            } 
        } catch (Exception e) {
        }
        return false ;
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if( CheckMa() == true ){
            JOptionPane.showMessageDialog(rootPane, "Ma trung roi");
            return;
        }
        
        try {
            double a = Double.valueOf(txtDiem.getText());
            if( a < 0 || a > 10 ){
                JOptionPane.showMessageDialog(rootPane, "Khoang diem tu 0 -> 10");
                return ;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Khong dc nhap chu");
            return ;
        }
        
        try {
            String sql = "Insert DIEM values ( ? , ? , ? )";
            try (   Connection con = ConnectSQL.OpenLinkSQL();
                    PreparedStatement stmt = con.prepareCall(sql);
                    ){
                    stmt.setString(1,  txtMaSV.getText());
                    stmt.setString(2,  cbbMaMon.getSelectedItem().toString() );
                    stmt.setFloat(3,  Float.valueOf(txtDiem.getText()));
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "Insert is seccessfull");
                    
            } catch (Exception e) {
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

