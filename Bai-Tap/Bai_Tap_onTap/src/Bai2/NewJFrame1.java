    private void DoVaoCBB(){
        try {
            try (   Connection con = ConnectSQL.OpenLink();
                    Statement stmt = con.createStatement();
                    ){
                   String sql = "select MaMH from MONHOC ";
                   ResultSet rs = stmt.executeQuery(sql);
                   
                   while ( rs.next() ) {                    
                       cbbMaMon.addItem(rs.getString(1));
                }
            } 
        } catch (Exception e) {
        }
    }
    
    private void HienThiNenTxt(){
        try {
            String sql = "select TenMH , diem from MONHOC join DIEM on DIEM.MaMH = MONHOC.MaMH where Masv = ? and DIEM.MaMH = ? ";
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareStatement(sql);
                    ){                 
                   stmt.setString(1, nv.MaSV);
                   stmt.setString(2, nv.MaMH);
                   ResultSet rs = stmt.executeQuery();
                   txtMaSV.setText(nv.MaSV);
                   cbbMaMon.setSelectedItem(nv.MaMH);                   
                   while ( rs.next() ) {                    
                       txtTenMon.setText(rs.getString(1));
                       txtDiem.setText(String.valueOf(rs.getFloat(2)));
                }
            } 
        } catch (Exception e) {
        }   
    }
    
    private void ClearTxt(){
        txtDiem.setText("");
        txtMaSV.setText("");
        txtTenMon.setText("");
        cbbMaMon.setSelectedIndex(0);
    }
    
