    private boolean CheckTrung(){
        try {
            try (   Connection con = ConnectSQL.OpenLink();
                    Statement stmt = con.createStatement();
                    ){
                    String sql = "select * from DIEM ";
                    ResultSet rs = stmt.executeQuery(sql);
                    
                    while ( rs.next() ) {                    
                         if( txtMaSV.getText().equalsIgnoreCase(rs.getString(1)) && cbbMaMon.getSelectedItem().toString().equalsIgnoreCase(rs.getString(2)))
                               return true ;
                }
                
            } 
        } catch (Exception e) {
        }
        return false ;
    }
    
    private boolean CheckDiem(){
        try {
            Double db = Double.valueOf(txtDiem.getText()) ;
            if( db >=0 && db <= 10 ) 
                return true ;
        } catch (Exception e) {
            return false;
        }
        return false ;
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if( CheckTrung() == true ){
            JOptionPane.showMessageDialog(rootPane, "ma trung roi");
            return ;
        }
        
        if( CheckDiem() == false ){
            JOptionPane.showMessageDialog(rootPane, "Diem Sai dinh dang");
            return ;
        }
        
        try {
            String sql = "insert DIEM values ( ? , ? , ? )";
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareStatement(sql);
                    ){                 
                    stmt.setString(1, txtMaSV.getText());
                    stmt.setString(2, cbbMaMon.getSelectedItem().toString() );
                    stmt.setFloat(3, Float.valueOf(txtDiem.getText()));
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "Insert is successfully");
            } 
        } catch (Exception e) {
        }  
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if( CheckTrung() == false ){
            JOptionPane.showMessageDialog(rootPane, "ma trung roi");
            return ;
        }
        
        if( CheckDiem() == false ){
            JOptionPane.showMessageDialog(rootPane, "Diem Sai dinh dang");
            return ;
        }      
        try {
            String sql = "Update DIEM set diem = ? where Masv = ? and MaMH = ? ";
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareStatement(sql);
                    ){                 
                    stmt.setString(2, txtMaSV.getText());
                    stmt.setString(3, cbbMaMon.getSelectedItem().toString() );
                    stmt.setFloat(1, Float.valueOf(txtDiem.getText()));
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "Update is successfully");
            } 
        } catch (Exception e) {
        }  
    }//GEN-LAST:event_jButton3ActionPerformed

