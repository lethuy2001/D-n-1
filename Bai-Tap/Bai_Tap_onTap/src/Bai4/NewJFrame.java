    private Boolean CheckTrung(){
        for( int i=0 ; i<table.getRowCount() ; i++ ){
            if( txtMaSach.getText().equalsIgnoreCase(table.getValueAt(i, 0).toString() ))
                return true ;
        }
        return false ;
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if( CheckTrung() == true ){
            JOptionPane.showMessageDialog(rootPane, "ma trung");
            return ;
        }
        
        try {
            TheLoai tl = (TheLoai) cbbTenTL.getSelectedItem() ;
            String sql = " insert into SACH (Masach, TenSach ,NXB ,SoLuong, NgayNhap ,MaTheLoai) values (? , ? , ? , ? ,? ,? )" ;
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareCall(sql);
                    ){
                    stmt.setString(2, txtTenSach.getText() );
                    stmt.setString( 5, txtNgayNhap.getText());
                    stmt.setFloat( 4 , Integer.valueOf(txtSoLuong.getText()));
                    stmt.setString(1, txtMaSach.getText() );
                    stmt.setString(3, "ttt" );
                    stmt.setString(6, tl.getMaTL() );
                    
                    stmt.executeUpdate();
                    DoVaoTable();
                    JOptionPane.showMessageDialog(rootPane, "Insert is Successfully");
            }
        } catch (Exception e) {
         }             
    }//GEN-LAST:event_jButton1ActionPerformed

