    private void DoVaoCbb(){
        try {
            try (   Connection con = ConnectSQL.OpenLink();
                    Statement stmt = con.createStatement();
                    ){
                    String sql = "select * from THELOAI" ;
                    ResultSet rs = stmt.executeQuery(sql);
                    
                    while ( rs.next() ) {                    
                        TheLoai tl = new TheLoai();
                        tl.setMaTL(rs.getString(1));
                        tl.setTenTL(rs.getString(2));
                        model_cbb.addElement(tl);
                }
                
            }
        } catch (Exception e) {
        }
      
    }
    
    private void DoVaoTable(){
        try {
            TheLoai tl = (TheLoai) cbbTenTL.getSelectedItem() ;
            String sql = "select Masach, TenSach , SoLuong, NgayNhap from SACH where MaTheLoai = ? " ;
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareCall(sql);
                    ){
                    stmt.setString(1, tl.getMaTL() );
                    ResultSet rs = stmt.executeQuery();
                    model_table.setRowCount(0);
                    while ( rs.next() ) {                    
                       model_table.addRow(new Object[] { rs.getString(1) , rs.getString(2) , rs.getString(4) , rs.getInt(3) });
                }
            }
        } catch (Exception e) {
        }   
    }
    
