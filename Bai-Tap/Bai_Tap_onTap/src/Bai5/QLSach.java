    private boolean CheckTrung(){
        for( int i=0 ; i<table.getRowCount() ; i++ ){
            if( txtMaSach.getText().equalsIgnoreCase(table.getValueAt(i, 0).toString()))
                return true ;
        }
        return false ;
    }
    
    private boolean CheckSo( String sk ){
        try {
            Double cb = Double.valueOf(sk); 
            if(cb > 0 ) 
                  return true ;
        } catch (Exception e) {
            return false ;
        }
        return false; 
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if( txtGiaTien.getText().isEmpty() || txtMaSach.getText().isEmpty() || txtNXB.getText().isEmpty() 
                ||txtSoLuong.getText().isEmpty() || txtTenSach.getText().isEmpty() ){
            JOptionPane.showMessageDialog(rootPane,  "Vui long nhap day du thong tin");
            return  ;            
        }
        
        
        if( CheckTrung() == true ){
            JOptionPane.showMessageDialog(rootPane,  "ma trung roi");
            return  ;
        }
        
        if( CheckSo(txtSoLuong.getText()) == false ){
            JOptionPane.showMessageDialog(rootPane,  "So Luong phai la so va > 0");
            return  ;       
        }
        
        if( CheckSo(txtGiaTien.getText()) == false ){
            JOptionPane.showMessageDialog(rootPane,  "Gia Tien phai la so va > 0");
            return  ;       
        }        
        
        try {
            QLS ql = (QLS) cbbTL.getSelectedItem();
            String sql = "insert SACH values( ? , ? ,? , null ,? , ? , null , null , ? ) " ;
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareCall(sql);
                    ) {
                   stmt.setString( 1,txtMaSach.getText());
                   stmt.setString( 2,txtTenSach.getText());
                   stmt.setString( 3,txtNXB.getText());
                   stmt.setInt(4, Integer.valueOf(txtSoLuong.getText()) );
                   stmt.setDouble(5, Double.valueOf(txtGiaTien.getText()));                                   
                   stmt.setString( 6 ,ql.getMaTL() );
                   stmt.executeUpdate();
                   DoVaotable();
                   JOptionPane.showMessageDialog(rootPane, "Insert is successfully");
            } 
        } catch (Exception e) {
            e.getStackTrace();
        }  
    }//GEN-LAST:event_jButton2ActionPerformed

