    private void DoVaoCBB(){
        try {
            try (   Connection con = ConnectSQL.OpenLink();
                    Statement stmt = con.createStatement();
                    ) {
                   String sql = "select * from THELOAI" ;
                   ResultSet rs = stmt.executeQuery(sql);
                   
                   while ( rs.next() ) {                    
                       QLS ql = new QLS();
                       ql.setMaTL(rs.getString(1));
                       ql.setTenTL(rs.getString(2));
                       model_cbb.addElement(ql);
                }
            } 
        } catch (Exception e) {
        }
     
    }
    
    private void DoVaotable(){
        try {
            QLS ql = (QLS) cbbTL.getSelectedItem();
            String sql = "select Masach ,TenSach, NXB ,SoLuong,Giatien from SACH where MaTheLoai = ?" ;
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareCall(sql);
                    ) {
                   stmt.setString( 1, ql.getMaTL());
                   ResultSet rs = stmt.executeQuery();
                   model_table.setRowCount(0);
                   while ( rs.next() ) {                    
                       Vector vs = new Vector();
                       vs.add(rs.getString(1));
                       vs.add(rs.getString(2));
                       vs.add(ql.getMaTL());
                       vs.add(rs.getString(3));
                       vs.add(rs.getString(4));
                       vs.add(rs.getString(5));
                       model_table.addRow(vs);
                }
            } 
        } catch (Exception e) {
        }      
    }
    
