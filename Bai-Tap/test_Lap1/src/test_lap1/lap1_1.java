    private void KiemTra(){
        StringBuilder sb = new StringBuilder();
        
        if( txtUserName.getText().equals("") ){
            sb.append("UserName is requized!\n");
            txtUserName.setBackground(Color.yellow);
        }else{
            txtUserName.setBackground(Color.white);
        }
        
        if( txtPassWord.getText().equals("") ){
            sb.append("PassWord is requized!\n");
            txtPassWord.setBackground(Color.yellow);
            txtPassWord.requestFocus();
        }else{
            txtPassWord.setBackground(Color.white);
        }
        
        if( txtComfirm.getText().equals("") ){
            sb.append("Comfirm is requized!");
            txtComfirm.setBackground(Color.yellow);
        }else{
            txtComfirm.setBackground(Color.white);
        }
        
        if( sb.length() > 0 ){
            JOptionPane.showMessageDialog(rootPane, sb.toString() );
            Index = 1 ;
            return;
        }
        Index = 0 ;
    }
    
    private void btnSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignInActionPerformed
        KiemTra();
        
        if( Index == 0 ){
            try {
                if( !txtPassWord.getText().equals(txtComfirm.getText())){
                     JOptionPane.showMessageDialog(rootPane, "PassWord have to equal Comfirm");
                     return ;
                }
            } catch (Exception e) {
            }
            JOptionPane.showMessageDialog(rootPane, "Sign up is scuccessfully");
        }
    }//GEN-LAST:event_btnSignInActionPerformed

