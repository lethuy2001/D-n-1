    public void KetNoi(){
        try {
            try (  Connection con = ConnectSQL.OpenLink();
                   Statement stmt = con.createStatement();
                    ) {
                  String sql = "select * from HOCSINH";
                  ResultSet rs = stmt.executeQuery(sql);
                  listHS.clear();
                  while (rs.next()) {                    
                    HocSinh hs = new HocSinh();
                    hs.setMaSV(rs.getString(1));
                    hs.setHoTen(rs.getString(2));
                    hs.setGioiTinh(rs.getString(3));
                    hs.setDiem(rs.getFloat(4));
                    listHS.add(hs);
                }
            }
        } catch (Exception e) {
        }
    }

