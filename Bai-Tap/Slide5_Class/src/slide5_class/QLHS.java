    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if( CheckMa() == false ){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập mã Sinh Bạn muốn Edit");
            return ;
        }
        

        try {
            String sql = "Update HOCSINH set TenHS = ? , GioiTinh = ? , DiemTB = ? where MaHS = ?";
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareCall(sql);
                    ){
                    stmt.setString( 4, txtMaHS.getText());
                    stmt.setString( 1, txtHoTen.getText());
                    stmt.setString( 2, CheckNam.isSelected() ? "Nam" : "Nữ");
                    stmt.setFloat(3, Float.valueOf(txtDiem.getText()) );
                    stmt.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "Bạn đã sửa thành công");
            } 
            KetNoi();
        } catch (Exception e) {
        }        
    }//GEN-LAST:event_btnUpdateActionPerformed

    public void DoNguoc( int k ){
        HocSinh sv = listHS.get(k);
        txtMaHS.setText(sv.getMaSV());
        txtHoTen.setText(sv.getHoTen());
        if( sv.getGioiTinh().equalsIgnoreCase("Nam")){
            CheckNam.setSelected(true);
        }else{
            CheckNu.setSelected(true);
        }
        txtDiem.setText(String.valueOf(sv.getDiem()));
        lbDien.setText( (k+1) + "/" + listHS.size());
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Index = 0 ;
        DoNguoc(Index);
    }//GEN-LAST:event_jButton1ActionPerformed

