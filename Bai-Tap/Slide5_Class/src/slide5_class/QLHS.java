    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            String sql = "Delete from HOCSINH where MaHS = ?";
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareStatement(sql);
                    ) {
                   stmt.setString(1, txtMaHS.getText());
                   stmt.executeUpdate();
                   JOptionPane.showMessageDialog(rootPane, "Bạn đã xóa thành công");
            }
            KetNoi();
        } catch (Exception e) {
            e.getStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    public boolean CheckMa(){
        for( HocSinh x : listHS ){
            if( x.getMaSV().equalsIgnoreCase(txtMaHS.getText())){
               return true ;
           }
        }
        return false ;
    }
    
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if( CheckMa() == true ){
            JOptionPane.showMessageDialog(rootPane, "Mã Đã Có rồi");
            return ;
        }
        
        if( txtDiem.getText().isEmpty() || txtMaHS.getText().isEmpty() || txtHoTen.getText().isEmpty() ){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đầy đủ thông tin");
            return ;
        }
        
        try {
            String sql = "Insert into HOCSINH values ( ? , ? , ? , ?)";
            try (   Connection con = ConnectSQL.OpenLink();
                    PreparedStatement stmt = con.prepareCall(sql);
                    ){
                    stmt.setString( 1, txtMaHS.getText());
                    stmt.setString( 2, txtHoTen.getText());
                    stmt.setString( 3, CheckNam.isSelected() ? "Nam" : "Nữ");
                    stmt.setFloat(4, Float.valueOf(txtDiem.getText()) );
                    int k = stmt.executeUpdate();
                    
                    if( k > 0 ) JOptionPane.showMessageDialog(rootPane, "Bạn đã thêm thành công");
            } 
            KetNoi();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAddActionPerformed

