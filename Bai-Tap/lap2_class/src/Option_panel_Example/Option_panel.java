    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String name = JOptionPane.showInputDialog(rootPane, "Nhập tên của bạn: ") ;
        
        if( name.length() > 0  ){
            JOptionPane.showMessageDialog(rootPane, "Tên Của bạn là: " + name );
        }else{
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tên");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int chose = JOptionPane.showConfirmDialog(rootPane, "Mời Bạn Lựa Chọn??", "Comfirm", JOptionPane.YES_NO_OPTION , JOptionPane.QUESTION_MESSAGE);
        
        if( chose == JOptionPane.YES_OPTION ){
            JOptionPane.showMessageDialog(rootPane, "Bạn Chọn Yes");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Bạn Chọn No");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String option[] = {"JAVA" , "C++" , "PHP" , "C#"};
       
       int k = JOptionPane.showOptionDialog(rootPane, "Mời bạn chọn", "Xác nhận", 0, JOptionPane.QUESTION_MESSAGE , null , option, option );
       
       JOptionPane.showMessageDialog(rootPane, "Bạn Chọn " + option[k] );
    }//GEN-LAST:event_jButton2ActionPerformed

