    private void DoVaoTree(){
        DefaultMutableTreeNode root = new DefaultMutableTreeNode("Courses");
        
        model = new DefaultTreeModel(root);
        
        DefaultMutableTreeNode note = new DefaultMutableTreeNode("Java Programming");
        root.add(note);
       
        DefaultMutableTreeNode note1 = new DefaultMutableTreeNode("PHP Programming");
        root.add(note1);

        DefaultMutableTreeNode note2 = new DefaultMutableTreeNode("C# Programming");
        root.add(note2);        
        
        tree.setModel(model);
    }
